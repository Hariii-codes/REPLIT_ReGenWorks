def seed_localization_strings():
    """Seed localization strings for all languages"""
    with app.app_context():
        try:
            count = 0
            for key, translations in COMMON_STRINGS.items():
                for lang_code, value in translations.items():
                    # Check if already exists
                    existing = LocalizationString.query.filter_by(
                        key=key,
                        language=lang_code
                    ).first()
                    
                    if not existing:
                        loc_string = LocalizationString(
                            key=key,
                            language=lang_code,
                            value=value,
                            context='both'
                        )
                        db.session.add(loc_string)
                        count += 1
            
            db.session.commit()
            print(f"[OK] Seeded {count} localization strings")
            return count
        except Exception as e:
            print(f"[ERROR] Error seeding strings: {e}")
            db.session.rollback()
            return 0

if __name__ == '__main__':
    seed_localization_strings()

